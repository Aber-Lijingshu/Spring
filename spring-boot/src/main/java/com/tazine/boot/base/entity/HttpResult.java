package com.tazine.boot.base.entity;

/**
 * HTTP请求最外层响应体
 *
 * @author frank
 * @since 1.0.0
 */
public class HttpResult<T> {

    private int code;

    private String msg;

    private T data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
